	{
		$data = Input::all();
		$today = date('Y-m-d');

		
		$discounts = Discount::where(function ($query) use ($today)  {
    		$query->where('fecha_inicio', '<=', $today)
          		  ->where('fecha_fin', '>=', $today);
		})->orWhere('sin_limites', '=', 'true')->get();
		
		return $discounts->toJson();
	}

